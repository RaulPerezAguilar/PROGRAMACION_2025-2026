numero1 = input("Dime un numero: \n") # Pedir numeros
numero2 = input("Dime otro numero: \n") 
suma = int(numero1) + int(numero2) # Convertir a entero y sumar
print("La suma de " + numero1 + " y " + numero2 + " es " + str(suma)) # Mostrar resultado, como suma es int, convertir a str (cadena) para concatenar